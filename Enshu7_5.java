public class Enshu7_5 {

	// 「こんにちは」と表示して改行するメソッドhello
	static void hello() {
		// 文章表示
		System.out.print("こんにちは。");
		// 改行
		System.out.println();
	}

	public static void main(String[] args) {
		// メソッドhelloの呼び出し
		hello();
	}
}
